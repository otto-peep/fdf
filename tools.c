#include "fdf.h"

void	ft_puttabnbr(int	**tab)
{
	int i;
	int y;

	i = 0;
	while (tab[i])
	{
		y = 0;
		while (tab[i][y])
		{
			ft_putnbr(tab[i][y]);
			y++;
		}
		i++;
	}
}

